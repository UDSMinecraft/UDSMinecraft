<h2>The Worlds of UDSMinecraft</h2>
<p>
    There are currently four worlds on UDSMinecraft, one of these being the
    Portal Hub which links the remaining three worlds, this is the world you
    will be in when you join for the first time.
</p>
<p>
    <a href='/survival'>Survival</a> world is pretty much what you would expect, normal Minecraft
    with some nice extra features courtesy of the UDSPlugin. Among these
    features are ranks, online currency, clans, home protections, player owned
    shops, clan bases and even player owned cities! There is always something
    new to do in Survival and if you can keep a secret: unless you have PvP
    enabled you will rarely drop any of your items on death!
</p>
<p>
    <a href='/creative'>Creative</a> world is for those who have uncontrollable imaginations, or those
    who just want a little break from Survival for a while. Claim a plot of land
    and build what you like.
</p>
<p>
    Coming soon to Creative world:
</p>
<ul>
    <li>The ability to expand your plot to adjacent plots.</li>
    <li>Voting and a 'Top 5 Plots' feature</li>
</ul>
<p>
    <a href='/minigames'>Minigames</a> world is a work in progress at the moment but it will feature a
    huge variety of arenas and challenge areas, with games ranging from PvP, mob
    arenas, parkour events and the Minecraft special - spleef!
</p>
