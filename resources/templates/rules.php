<div id='rules' class='thin'>
    <h3>No Swearing/Racism/Flaming</h3>
    <ul>
        <li>
            Please refrain from using foul language or making racist comments in
            public chat, this server does not have an age limit and many very
            young players may be online.
        </li>
        <li>
            Swearing will result in a censor and a 1 credit fine. If you do not
            have any money you will be put in jail for a minute.</li>
        <li>
            Repeat offenses may result in a kick or a ban.
        </li>
        <li>
            Chat spam (speaking very quickly in public chat) will always result
            in a kick.
        </li>
        <li>
            Flaming is the act of insulting someone or something, extensive
            flaming will result in a ban.
        </li>
        <li>
            Heated arguments or debates also fall under the category of flaming
            (known as a flame war), please keep these conversations in a private
            chat. Ignoring this rule will result in jail time.
        </li>
    </ul>
    <h3>No Griefing</h3>
    <ul>
        <li>
            Griefing is not limited to destroying houses or placing blocks,
            griefing is the act of causing grief.
        </li>
        <li>
            Please do not cause any other players to become uncomfortable or
            actively attempt to cause distress.
        </li>
        <li>
            Griefing will result in extensive jail time, repeat offenses will
            result in a ban.
        </li>
    </ul>
    <h3>No Pet Killing</h3>
    <ul>
        <li>
            Unless you are engaged in PVP (player vs player combat) pet killing
            is frowned upon.
        </li>
        <li>
            If you see a tamed pet on the server and it is in your way please
            contact a member of staff (dark blue, yellow and gold names), do not
            attack it.
        </li>
        <li>
            Pet killing will result in jail time.
        </li>
    </ul>
    <h3>No Bug Exploits</h3>
    <ul>
        <li>
            If you are found to have exploited a bug either in the server or the
            plugin to gain profit or advantage over other players in some way
            you will be jailed and fined and if the bug resulted in you gaining
            extra items these will be confiscated.
        </li>
        <li>
            If you find a bug please submit a ticket using the /ticket command,
            you will receive respect and trust from our staff and we will be
            able to fix the bug or find a work around.
        </li>
        <li>
            If there is another player that you suspect has broken this rule
            please inform a member of staff (dark blue, yellow and gold names).
        </li>
    </ul>
    <h3>No Advertising</h3>
    <ul>
        <li>
            We appreciate it is difficult to find cheap advertising spaces for
            your product/service but please do not advertise on this server.
            Advertising will result in a ban.
        </li>
        <li>
            We allow players to advertise their own Minecraft servers but only
            while it does not decrease the enjoyment of other players. This
            decision is made at the discretion of staff.
        </li>
    </ul>
    <h3>No Hacks</h3>
    <ul>
        <li>
            Using any sort of client modification while on the server to gain
            profit or advantage over other players in some way will result in a
            ban.
        </li>
        <li>
            If there is another player that you suspect has broken this rule
            please inform a member of staff (dark blue, yellow and gold names).
        </li>
        <li>
            If you believe you need to use a hack for some purpose please first
            discuss it with a member of staff (dark blue, yellow and gold names)
            to avoid being banned.
        </li>
        <li>
            Although the use of some client modifications are difficult if not
            impossible to prove, please be aware that these are still frowned
            upon and any action taken is at the discretion of staff.
        </li>
    </ul>
</div>
